def run_PCA_identity_manifold():
    """
    Assemble all observations into a matrix.
    Replace NaN with column means for PCA.
    Run PCA. Compute biological and abiotic
    centroids in PC space.
    Compute Identity Distance Score (IDS) for
    each observation.
    """
    labels = list(OBSERVATION_VECTORS.keys())
    X_raw = np.array(
        [OBSERVATION_VECTORS[k] for k in labels]
    )
    types = []
    for lab in labels:
        if "Earth" in lab or "reference" in lab.lower():
            if ("audaxviator" in lab or
                    "FeOB" in lab or
                    "methanogen" in lab):
                types.append("biological_reference")
            elif "Serp" in lab or "photolysis" in lab:
                types.append("abiotic_reference")
            else:
                types.append("abiotic_reference")
        elif "Lafayette" in lab or "Webster" in lab:
            types.append("mars_observation")
        elif "SAM" in lab:
            types.append("mars_observation")
        elif "Serp" in lab or "photolysis" in lab or \
             "methane" in lab:
            if "Deep subsurface" in lab:
                types.append("biological_reference")
            else:
                types.append("abiotic_reference")
        else:
            types.append("mars_observation")

    # Impute NaN with column median of non-NaN values
    X_imp = X_raw.copy()
    for j in range(X_imp.shape[1]):
        col = X_imp[:, j]
        mask = ~np.isnan(col)
        if mask.sum() > 0:
            col_med = np.median(col[mask])
            X_imp[np.isnan(X_imp[:, j]), j] = col_med

    # Standardise
    scaler = StandardScaler() if SKLEARN_AVAILABLE \
        else None
    if SKLEARN_AVAILABLE:
        X_scaled = scaler.fit_transform(X_imp)
        pca = PCA(n_components=min(3, X_imp.shape[1]))
        X_pc = pca.fit_transform(X_scaled)
        explained = pca.explained_variance_ratio_
        loadings  = pca.components_
    else:
        # Manual PCA via SVD
        X_c = X_imp - X_imp.mean(axis=0)
        X_s = X_c / (X_imp.std(axis=0) + 1e-12)
        U, S, Vt = np.linalg.svd(X_s, full_matrices=False)
        X_pc = U * S
        n_comp = min(3, X_s.shape[1])
        X_pc = X_pc[:, :n_comp]
        S_var = S ** 2
        explained = S_var[:n_comp] / S_var.sum()
        loadings = Vt[:n_comp]

    # Biological centroid (from reference points)
    bio_idx = [i for i, t in enumerate(types)
               if t == "biological_reference"]
    abi_idx = [i for i, t in enumerate(types)
               if t == "abiotic_reference"]

    if len(bio_idx) > 0 and X_pc.shape[1] >= 2:
        bio_centroid = X_pc[bio_idx, :2].mean(axis=0)
    else:
        bio_centroid = np.zeros(2)
    if len(abi_idx) > 0:
        abi_centroid = X_pc[abi_idx, :2].mean(axis=0)
    else:
        abi_centroid = np.zeros(2)

    # Identity Distance Score:
    # For each observation in PC1-PC2 space,
    # compute signed projection onto the
    # bio→abiotic direction.
    # Positive = towards biological centroid.
    # Negative = towards abiotic centroid.

    direction = bio_centroid - abi_centroid
    norm = np.linalg.norm(direction)
    if norm > 1e-10:
        direction_unit = direction / norm
    else:
        direction_unit = np.array([1.0, 0.0])

    # Midpoint = separatrix in this projection
    midpoint = (bio_centroid + abi_centroid) / 2.0

    IDS = {}
    for i, lab in enumerate(labels):
        if X_pc.shape[1] >= 2:
            vec = X_pc[i, :2] - midpoint
        else:
            vec = X_pc[i, :1] - midpoint[:1]
            direction_unit = direction_unit[:1]
        score = float(np.dot(vec, direction_unit))
        IDS[lab] = {
            "PC1": float(X_pc[i, 0]),
            "PC2": float(X_pc[i, 1])
                   if X_pc.shape[1] > 1 else 0.0,
            "IDS": score,
            "type": types[i],
        }

    return {
        "labels":          labels,
        "types":           types,
        "X_pc":            X_pc,
        "explained":       explained,
        "loadings":        loadings,
        "bio_centroid":    bio_centroid,
        "abi_centroid":    abi_centroid,
        "IDS":             IDS,
        "direction_unit":  direction_unit,
        "midpoint":        midpoint,
    }

