def run_source_product_slope():
    """
    Fit slope of δ¹³C(product) vs δ¹³C(source)
    for all available Mars pairs.
    Test H₀: slope = 1.0 (biological invariant).
    """
    sources  = np.array([p[2] for p in MARS_ISOTOPE_PAIRS])
    products = np.array([p[4] for p in MARS_ISOTOPE_PAIRS])
    s_unc    = np.array([p[3] for p in MARS_ISOTOPE_PAIRS])
    p_unc    = np.array([p[5] for p in MARS_ISOTOPE_PAIRS])

    # Weighted ODR-style fit using scipy
    # Simple OLS first for comparison
    slope_ols, intercept_ols, r, pval, se = (
        stats.linregress(sources, products)
    )

    # Bootstrap slope to get CI
    n = len(sources)
    boot_slopes = []
    for _ in range(50_000):
        idx = np.random.randint(0, n, n)
        s_b = sources[idx] + np.random.normal(
            0, s_unc[idx])
        p_b = products[idx] + np.random.normal(
            0, p_unc[idx])
        try:
            sl, _, _, _, _ = stats.linregress(s_b, p_b)
            boot_slopes.append(sl)
        except Exception:
            pass
    boot_slopes = np.array(boot_slopes)

    slope_med = np.median(boot_slopes)
    slope_lo  = np.percentile(boot_slopes, 2.5)
    slope_hi  = np.percentile(boot_slopes, 97.5)

    # Fraction of bootstrap samples consistent
    # with biological invariant (slope = 1.0 ± 0.05)
    P_bio_slope = float(np.mean(
        np.abs(boot_slopes - BIO_SLOPE) < BIO_SLOPE_TOL
    ))

    # Signed distance from slope = 1.0
    # Positive: towards biological
    # Negative: towards abiotic
    signed_dist = slope_med - BIO_SLOPE

    return {
        "slope_ols":         float(slope_ols),
        "intercept_ols":     float(intercept_ols),
        "slope_median":      float(slope_med),
        "slope_CI_lo":       float(slope_lo),
        "slope_CI_hi":       float(slope_hi),
        "P_bio_slope":       P_bio_slope,
        "signed_dist_from_1": float(signed_dist),
        "r_value":           float(r),
        "sources":           sources,
        "products":          products,
        "s_unc":             s_unc,
        "p_unc":             p_unc,
        "boot_slopes":       boot_slopes,
    }

