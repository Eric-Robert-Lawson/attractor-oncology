def run_CN_d13C_analysis():
    """
    For each observation in C:N – δ¹³C space,
    compute signed distance from separatrix (C:N = 20).
    Positive = biological side (C:N < 20).
    Negative = abiotic side (C:N > 20).
    """
    results = []
    for entry in CN_D13C_DATA:
        label, cn_mid, cn_unc, d13c_mid, d13c_unc, \
            otype, color = entry

        # Monte Carlo for this observation
        cn_samples = np.random.normal(
            cn_mid, cn_unc, N_MC
        )
        cn_samples = np.clip(cn_samples, 0.1, None)

        # Signed distance from separatrix in log10(C:N)
        # Biological: log10(C:N) < log10(20) = 1.301
        # Abiotic:    log10(C:N) > 1.301
        log_CN = np.log10(cn_samples)
        log_sep = math.log10(CN_SEPARATRIX)
        signed_dist = log_sep - np.median(log_CN)
        # Positive: median < separatrix → biological side

        P_bio = float(np.mean(log_CN < log_sep))

        results.append({
            "label":      label,
            "cn_mid":     cn_mid,
            "cn_unc":     cn_unc,
            "d13c_mid":   d13c_mid,
            "d13c_unc":   d13c_unc,
            "type":       otype,
            "color":      color,
            "log_CN_med": float(np.median(log_CN)),
            "signed_dist":float(signed_dist),
            "P_bio_CN":   P_bio,
        })
    return results

