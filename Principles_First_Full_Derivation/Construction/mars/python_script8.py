# MODULE 4: COMPONENT 3 — SEASONAL CH₄ CURVATURE
# ─────────────────────────────────────────────────────────────

def arrhenius_model(T, log_A, Ea):
    """Rate ∝ A × exp(-Ea / RT)."""
    return log_A - Ea / (R_gas * T)

def langmuir_model(T, log_K0, dH):
    """
    Langmuir desorption: K ∝ exp(-ΔH_ads / RT).
    CH₄ in atmosphere ∝ K(T) at equilibrium.
    """
    return log_K0 + dH / (R_gas * T)

def run_seasonal_curvature():
    """
    Fit Arrhenius (biological) and Langmuir (abiotic)
    models to Webster 2018 CH₄ vs T seasonal data.
    Compare AIC, compute curvature of each trajectory.
    """
    T   = WEBSTER_DATA["T_surf_K"]
    CH4 = WEBSTER_DATA["CH4_ppbv"]
    log_CH4 = np.log(CH4)
    inv_T = 1.0 / T

    # ── Arrhenius fit ────────────────────────────────────────
    try:
        popt_arr, pcov_arr = optimize.curve_fit(
            arrhenius_model, T, log_CH4,
            p0=[10.0, 50000.0],
            maxfev=10000
        )
        log_A_fit, Ea_fit = popt_arr
        log_CH4_pred_arr = arrhenius_model(
            T, log_A_fit, Ea_fit
        )
        ss_res_arr = np.sum(
            (log_CH4 - log_CH4_pred_arr) ** 2
        )
        ss_tot = np.sum(
            (log_CH4 - np.mean(log_CH4)) ** 2
        )
        r2_arr = 1 - ss_res_arr / ss_tot
        n_params_arr = 2
        n = len(T)
        aic_arr = (n * np.log(ss_res_arr / n)
                   + 2 * n_params_arr)
    except Exception as e:
        Ea_fit = Ea_bio_J
        r2_arr = float("nan")
        aic_arr = float("nan")
        log_A_fit = 0.0
        log_CH4_pred_arr = log_CH4

    # ── Langmuir/van't Hoff fit ──────────────────────────────
    try:
        popt_lng, pcov_lng = optimize.curve_fit(
            langmuir_model, T, log_CH4,
            p0=[-5.0, 15000.0],
            maxfev=10000
        )
        log_K0_fit, dH_fit = popt_lng
        log_CH4_pred_lng = langmuir_model(
            T, log_K0_fit, dH_fit
        )
        ss_res_lng = np.sum(
            (log_CH4 - log_CH4_pred_lng) ** 2
        )
        r2_lng = 1 - ss_res_lng / ss_tot
        n_params_lng = 2
        aic_lng = (n * np.log(ss_res_lng / n)
                   + 2 * n_params_lng)
        delta_AIC = aic_arr - aic_lng
    except Exception:
        dH_fit = DH_abio_J
        r2_lng = float("nan")
        aic_lng = float("nan")
        delta_AIC = float("nan")
        log_K0_fit = 0.0
        log_CH4_pred_lng = log_CH4

    # ── Curvature in Arrhenius plot ──────────────────────────
    # Curvature = |d²(ln CH₄)/d(1/T)²|
    # For Arrhenius: linear in 1/T → curvature = Ea (slope)
    # For Langmuir:  linear in 1/T → curvature = ΔH (slope)
    # The RATIO of curvatures = Ea / ΔH_ads
    curvature_ratio = Ea_bio_J / DH_abio_J

    # ── Winning model ────────────────────────────────────────
    if not math.isnan(aic_arr) and not math.isnan(aic_lng):
        if aic_arr < aic_lng:
            winner = "Arrhenius (biological)"
            delta = aic_lng - aic_arr
        elif aic_lng < aic_arr:
            winner = "Langmuir (abiotic)"
            delta = aic_arr - aic_lng
        else:
            winner = "indeterminate"
            delta = 0.0
    else:
        winner = "fit_failed"
        delta = float("nan")

    return {
        "T":                T,
        "CH4":              CH4,
        "log_CH4":          log_CH4,
        "Ea_fit_J":         float(Ea_fit),
        "r2_Arrhenius":     float(r2_arr),
        "AIC_Arrhenius":    float(aic_arr),
        "log_A_fit":        float(log_A_fit),
        "log_CH4_pred_arr": log_CH4_pred_arr,
        "dH_fit_J":         float(dH_fit),
        "r2_Langmuir":      float(r2_lng),
        "AIC_Langmuir":     float(aic_lng),
        "log_K0_fit":       float(log_K0_fit),
        "log_CH4_pred_lng": log_CH4_pred_lng,
        "delta_AIC":        float(delta_AIC),
        "winner":           winner,
        "curvature_ratio":  float(curvature_ratio),
        "Ea_bio_J":         Ea_bio_J,
        "DH_abio_J":        DH_abio_J,
    }

